    private boolean validateInputs() {
        userId = txtUserId.getText();

        pwd = txtPassword.getPassword();
        retypePwd = txtRetypePassword.getPassword();

        if (userId.isEmpty() || pwd.toString().trim().isEmpty() || retypePwd.toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all the text fields", "FILL ALL!!!", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (userId.isEmpty() == false && pwd.length != 0 && retypePwd.length != 0) {
            if (Arrays.equals(pwd, retypePwd)) {
                return true;
            }
        }
        JOptionPane.showMessageDialog(null, "Please provide correct information!", "FILL ALL CORRECTLY!!!", JOptionPane.ERROR_MESSAGE);

        return false;

    }

