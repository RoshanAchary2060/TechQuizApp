    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (validateInputs() == false) {
            return;
        }
        try {
            User user = new User();
            user.setUserId(userId);
            user.setPassword(String.valueOf(pwd));
            user.setUserType("student");
            boolean result = UserDAO.addUser(user);
            if (result) {
                JOptionPane.showMessageDialog(null, "Registration Successfullly Done...!", "SUCCESS!", JOptionPane.INFORMATION_MESSAGE);
                int ans = JOptionPane.showConfirmDialog(null, "Another Registration?","Again?", JOptionPane.YES_NO_OPTION);
                if(ans == JOptionPane.YES_OPTION) {
                    txtUserId.setText("");
                    txtPassword.setText("");
                    txtRetypePassword.setText("");
                    txtUserId.requestFocus();
                } else {
                    AdminOptionsFrame adm = new AdminOptionsFrame();
                    adm.setVisible(true);
                    this.dispose();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Can't register student right now!", "DB ERROR!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

